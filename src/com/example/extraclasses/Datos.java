package com.example.extraclasses;

public class Datos {
	private String clave;
	private String valor;
	
	public Datos(){}
	
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
}
