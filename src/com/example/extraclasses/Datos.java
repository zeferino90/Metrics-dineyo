package com.example.extraclasses;

public class Datos {
	private String key;
	private String value;
	
	public Datos(){}
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
